export default function base62(n: number) {
  if (n === 0) {
    return dictionary[n]
  }

  let s: string[] = []
  while (n > 0) {
    s.push(dictionary[n % 62])
    n = Math.floor(n / 62)
  }

  return s.reverse().join('')
}
